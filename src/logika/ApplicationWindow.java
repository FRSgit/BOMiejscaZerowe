        /**
        * Loads desired application's state to `mainPanel` jPanel
        */
        private void moveToState(int stateId){
            switch(stateId){
                case STATE_SIMULATION:
                    moveToSimulation();
                    break;
                case STATE_AUTHORS:
                    moveToAuthors();
                    break;
                case STATE_MENU:
                default:
                    moveToMenu();
                    break;    
            }
            mainPanel.revalidate();
            mainPanel.repaint();
        }
        
        /**
         * Moves to main menu
         */
        private void moveToMenu(){
            mainPanel.removeAll();

            mainPanel.add(mainMenuPane);
        }
        
        /**
         * moves to simulation screen
         */
        private void moveToSimulation(){
            mainPanel.removeAll();

            mainPanel.add(simulationPane);
        }
        
        /**
         * moves to authors screen
         */
        private void moveToAuthors(){
            mainPanel.removeAll();

            mainPanel.add(authorsPane);
        }

    
